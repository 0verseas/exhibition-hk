function generateLink(id){
  let link = "https://student.overseas.ncnu.edu.tw/quota/";
  switch(radios){
    case '1':
      link += "index.html?school="+id+"&group=all&keyword=&first-group=true&second-group=true&third-group=true";
      break;
    case '2':
        link += "two-year.html?school="+id+"&group=all&keyword=";
      break;
    case '3':
        link += "master.html?school="+id+"&group=all&keyword=";
      break;
    case '4':
        link += "phd.html?school="+id+"&group=all&keyword=";
      break;
    default:
        link = "https://student.overseas.ncnu.edu.tw/quota/index.html?school="+id+"&group=all&keyword=&first-group=true&second-group=true&third-group=true";
  }
  return link;
}

